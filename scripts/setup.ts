async function checkDockerRunning(): Promise<boolean> {
  try {
    execSync('docker info', { stdio: 'pipe' });
    return true;
  } catch {
    return false;
  }
}

async function startDockerServices(): Promise<void> {
  log('Starting Docker services...');

  if (!await checkDockerRunning()) {
    log('Docker is not running. Please start Docker Desktop and run setup again.', 'error');
    process.exit(1);
  }

  try {
    // Start services in detached mode
    execSync('docker-compose up -d', {
      cwd: ROOT_DIR,
      stdio: 'inherit',
    });
    log('Docker services started', 'success');
  } catch (error) {
    log(`Failed to start Docker services: ${error}`, 'error');
    process.exit(1);
  }
}

async function verifyPostgresCredentials(): Promise<void> {
  // After pg_isready confirms the server is accepting connections, attempt a
  // credentialed connection to catch a stale Postgres volume whose
  // superuser password doesn't match the current .env.
  const { Client } = require('pg') as typeof import('pg');
  const client = new Client({
    host: process.env.DB_HOST || 'localhost',
    port: Number(process.env.DB_PORT || 5432),
    user: process.env.DB_USER || 'hubblewave',
    password: process.env.DB_PASSWORD,
    database: process.env.DB_NAME || 'hubblewave',
    connectionTimeoutMillis: 3000,
  });
  try {
    await client.connect();
    await client.end();
  } catch (err: unknown) {
    const pgErr = err as { code?: string; message?: string };
    if (pgErr.code === '28P01') {
      // password authentication failed for user
      log('', 'error');
      log('PostgreSQL rejected the credentials from .env (28P01: password mismatch).', 'error');
      log('Your Postgres volume likely has credentials from a prior setup run.', 'error');
      log('Fix: `docker-compose down -v && npm run setup`', 'error');
      log('     (The -v flag drops the data volume so Postgres reinitializes with current .env)', 'error');
      log('     Or run: npm run setup:fresh  (drops volume + deletes .env + re-runs setup)', 'error');
      process.exit(1);
    }
    if (pgErr.code === '3D000') {
      // database does not exist yet — this is fine on first run; migrations create it
      return;
    }
    // Any other connection error: log + exit so the failure is visible
    log(`Postgres credential check failed: ${pgErr.message ?? String(err)}`, 'error');
    process.exit(1);
  }
}

async function waitForPostgres(maxAttempts: number = 30): Promise<void> {
  log('Waiting for PostgreSQL to be ready...');

  for (let i = 0; i < maxAttempts; i++) {
    try {
      const dbUser = process.env.DB_USER || 'hubblewave';
      const dbName = process.env.DB_NAME || 'hubblewave';
      execSync(`docker exec hw_postgres pg_isready -U ${dbUser} -d ${dbName}`, {
        stdio: 'pipe',
      });
      log('PostgreSQL is ready', 'success');
      // Verify the credentials from .env actually work so we can surface
      // a stale-volume 28P01 error with a clear recovery hint (Issue 5).
      await verifyPostgresCredentials();
      return;
    } catch {
      process.stdout.write('.');
      await new Promise(resolve => setTimeout(resolve, 1000));
    }
  }

  log('PostgreSQL did not become ready in time', 'error');
  process.exit(1);
}


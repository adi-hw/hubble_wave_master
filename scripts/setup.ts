async function printSummary(options: SetupOptions): Promise<void> {
  const skippedLines: string[] = [];
  if (options.skipAva) {
    skippedLines.push('  - AVA Runtime (Ollama) — skipped via --skip-ava');
  }
  if (options.skipSearch) {
    skippedLines.push('  - TypeSense bootstrap — skipped via --skip-search');
  }
  const skippedBlock = skippedLines.length > 0
    ? `\nSkipped services:\n${skippedLines.join('\n')}\n`
    : '';

  console.log('\n' + '='.repeat(60));
  console.log('\x1b[32m Setup Complete! \x1b[0m');
  console.log('='.repeat(60));
  console.log(`
Services running:
  - PostgreSQL:  localhost:5432
  - Redis:       localhost:6379
  - MinIO:       localhost:9000 (Console: localhost:9001)
  - TypeSense:   localhost:8108
  - AVA Runtime: localhost:11434 (Ollama)
${skippedBlock}
Credentials:
  - Admin, database, storage, and TypeSense credentials are stored in .env
  - Admin password is printed by seed-admin-user.ts during setup

Next steps:
  1. Start the full platform (api + worker + web-client):
     npm run dev:platform

  2. Or start everything (incl. control-plane + web-control-plane):
     npm run dev:all

  3. Or start services individually:
     npm run dev:api               # Modular monolith API (port 3000)
     npm run dev:worker            # BullMQ worker
     npm run dev:web               # Web client (Vite — port printed at start)
     npm run dev:control-plane     # Control plane API
     npm run dev:web-control-plane # Control plane admin console

  4. Access the application:
     http://localhost:4200 (or whatever port Vite prints at start)
`);
  console.log('='.repeat(60) + '\n');
}

async function main(): Promise<void> {
  console.log('\n============================================================');
  console.log('  HubbleWave Platform Development Setup (Single Instance)');
  console.log('============================================================\n');

  const args = process.argv.slice(2);
  const options: SetupOptions = {
    skipDocker: args.includes('--skip-docker'),
    skipEnv: args.includes('--skip-env'),
    skipAva: args.includes('--skip-ava'),
    skipSearch: args.includes('--skip-search'),
  };

  try {
    await installDependencies();

    if (!options.skipEnv) {
      await setupEnvFiles();
    } else {
      const envPath = path.join(ROOT_DIR, '.env');
      if (fs.existsSync(envPath)) {
        dotenv.config({ path: envPath, override: false });
      }
    }

    if (!options.skipDocker) {
      await startDockerServices();
    }

    await waitForPostgres();
    await waitForRedis();
    await waitForHttpService('MinIO', 'http://localhost:9000/minio/health/live');

    if (!options.skipSearch) {
      await waitForHttpService('TypeSense', 'http://localhost:8108/health');
    } else {
      log('Skipping TypeSense wait (--skip-search)', 'warn');
    }

    if (!options.skipAva) {
      await waitForHttpService('AVA Runtime', 'http://localhost:11434/api/tags', 90);
    } else {
      log('Skipping AVA Runtime wait (--skip-ava)', 'warn');
    }

    await runMigrations();

    await bootstrapBuckets();

    if (!options.skipSearch) {
      await bootstrapTypesense();
    } else {
      log('Skipping TypeSense bootstrap (--skip-search)', 'warn');
    }

    await seedAdminUser();

    if (!options.skipAva) {
      await seedPlatformKnowledge();
    } else {
      log('Skipping platform knowledge seed (--skip-ava)', 'warn');
    }

    await printSummary(options);

  } catch (error) {
    log(`Setup failed: ${error}`, 'error');
    process.exit(1);
  }
}

main();
